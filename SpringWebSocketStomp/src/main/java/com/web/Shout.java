package com.web;

public class Shout {

	String msg;
	
	public Shout() {};
	public Shout(String s) { this.msg = s; };

	/**
	 * @return the msg
	 */
	public String getMsg() {
		return msg;
	}

	/**
	 * @param msg the msg to set
	 */
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
}
